export type Difficulty = 'NORMAL' | 'HARD';

export function getSafeWordPairs(pairs: WordPair[] | undefined): WordPair[] {
  return Array.isArray(pairs)
    ? pairs.filter((pair) => pair && typeof pair.main === 'string' && typeof pair.similar === 'string')
    : [];
}

const WORD_PAIRS = WORD_THEMES.flatMap((theme) => getSafeWordPairs(theme.pairs));
const HARD_WORD_PAIRS = WORD_THEMES.flatMap((theme) => getSafeWordPairs(theme.hardPairs));

export function getAvailableWordPairs(selectedTheme: Theme | null, difficulty: Difficulty): WordPair[] {
  if (!selectedTheme) {
    return difficulty === 'HARD' ? HARD_WORD_PAIRS : WORD_PAIRS;
  }

  const normalPairs = getSafeWordPairs(selectedTheme.pairs);
  const hardPairs = getSafeWordPairs(selectedTheme.hardPairs);
  const defaultPairs = difficulty === 'HARD' ? HARD_WORD_PAIRS : WORD_PAIRS;

  if (difficulty === 'HARD') {
    return hardPairs.length > 0 ? hardPairs : normalPairs;
  }

  return normalPairs.length > 0 ? normalPairs : defaultPairs;
}
